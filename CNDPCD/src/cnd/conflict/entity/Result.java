package cnd.conflict.entity;

public class Result {

	int policyAmount;
	int conflictAmount;
	int unrelation;
	int syntaxconflict;
	int inclusivematch;
	int partialmatch;
	public int getPolicyAmount() {
		return policyAmount;
	}
	public void setPolicyAmount(int policyAmount) {
		this.policyAmount = policyAmount;
	}
	public int getConflictAmount() {
		return conflictAmount;
	}
	public void setConflictAmount(int conflictAmount) {
		this.conflictAmount = conflictAmount;
	}
	public int getUnrelation() {
		return unrelation;
	}
	public void setUnrelation(int unrelation) {
		this.unrelation = unrelation;
	}
	public int getSyntaxconflict() {
		return syntaxconflict;
	}
	public void setSyntaxconflict(int syntaxconflict) {
		this.syntaxconflict = syntaxconflict;
	}
	public int getInclusivematch() {
		return inclusivematch;
	}
	public void setInclusivematch(int inclusivematch) {
		this.inclusivematch = inclusivematch;
	}
	public int getPartialmatch() {
		return partialmatch;
	}
	public void setPartialmatch(int partialmatch) {
		this.partialmatch = partialmatch;
	}
	
}
